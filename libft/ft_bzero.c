#include "libft.h"

int	ft_bzero(void *s, int n)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = (char *)s;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
	return (0);
}
